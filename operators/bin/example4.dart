void main(List<String> args) {
  ///Working with binary infix operators
  ///Mathematical bitwise infix operators
  var age = 25;
  print(age + 20); //Addition
  print(age - 20);
  print(age * 20);
  print(age / 20);
  print(age ~/ 20);
  print(age % 20);

  ///Comparison operator
  print(age < 20);
  print(age > 20);
  print(age <= 20);
  print(age >= 20);

  //Assignment operators
  print(age == 20);
  print(age != 20);

  //Logical
  print(age & 20);
  print(age | 20);
  print(age ^ 20);

  //SHif
  print(age >> 20);
  print(age << 20);
}