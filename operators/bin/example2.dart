void main(List<String> args) {
  ///Lets explore different types of operators in dart
  /// 1. Unary prefix operators
  /// 2. Unary postfix operator
  /// 3. Binary infix
  /// 4. compound assignment operator
  /// 
  /// . 1. Unary prefix - 
  int number = 80;
  print(--number); //Internaly changes the value
  print(++number);
  print(-number);
  print(!false);
  print(!!true);
  print(!!false);
  print(~3);
}