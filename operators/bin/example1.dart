void main(List<String> args) {
  /// Let's explore basic math operators
  final num1 = 50;
  final num2 = 23;

  print('--Addition--');
  print(num1 + num2);
  print('--Subtraction--');
  print(num1 - num2);
  print('--Multiplication');
  print(num1 * num2);
  print('--Division--');
  print(num1 / num2);
  print('Truncating Division--');
  print(num1 ~/ num2);
  print('--Modulus operation');
  print(num1 % num2);
}

